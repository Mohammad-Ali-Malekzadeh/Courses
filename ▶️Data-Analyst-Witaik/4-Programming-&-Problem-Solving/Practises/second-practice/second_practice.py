    def encoder(text):
        user_words = text.split()
        i = 0
        while i < len(user_words):
            if user_words[i] in words.keys():
                user_words[i] = words[user_words[i]]
            i += 1
        result = ' '.join(user_words)
        print(f'Your text has been encrypted. here you are:\n{result}')

    def decoder(text):
        user_words = text.split()
        i = 0
        while i < len(user_words):
            if user_words[i] in words.keys():
                user_words[i] = list(filter(lambda x: words[x] == user_words[i], words))[0]
            i += 1
        result = ' '.join(user_words)
        print(f'Your text has been decrypted. here you are:\n{result}')

